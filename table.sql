create table flight_data(
    PassengerID int,
    PassengerName varchar(20) not null,
    PassengerPhoneNo varchar(20) not null,
    PassengerEmail varchar(255) not null,
    PassengerSex varchar(20) not null,
    PassengerSourceCity varchar(20) not null,
    PassengerDestinationCity varchar(20) not null
    BookingDate DATE not null,
);

